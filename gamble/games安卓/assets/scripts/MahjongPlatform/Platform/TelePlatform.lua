require("MahjongCommon/CustomNode");
require("MahjongPlatform/Platform/BasePlatform");

TelePlatform = class(BasePlatform);


TelePlatform.ctor = function ( self)
	self.curDefaultPmode = 117; --默认为天翼的商品
	self.m_loginTable = {PlatformConfig.QQLogin, PlatformConfig.BoyaaLogin,PlatformConfig.GuestLogin};
end

TelePlatform.dtor = function ( self )
end

-- 获取应用APPID信息
-- @Override
TelePlatform.getLoginAppId = function( self, loginType )
	if PlatformConfig.GuestLogin == loginType or PlatformConfig.BoyaaLogin == loginType then
		return "301";

	elseif PlatformConfig.QQLogin == loginType then
		return "300";

	elseif PlatformConfig.SinaLogin == loginType then
		return "302";
	end
end

TelePlatform.returnIsLianyunName = function(self)
	return PlatformConfig.feedback_platform_tele;
end

--是否显示绑定博雅通行证
TelePlatform.isNeedToShowBYPassCard = function ( self )
	if self:getCurrentLoginType() == PlatformConfig.GuestLogin then
		return true;
	end
end

TelePlatform.changeLoginMethod = function(self,loginMethod)
	return new(self.getLoginMethodCls(loginMethod) or GuestLogin);
end

TelePlatform.getLoginView = function ( self,hallRef )
	return new(TelePlatformView,self.m_loginTable);
end

TelePlatform.getDefaultLoginMethod = function ( self )
	local loginType = g_DiskDataMgr:getAppData(kLastLoginType, PlatformConfig.GuestLogin);
	return loginType;
end

TelePlatform.isSupportQuickRecharge = function ( self )
	return true;
end

